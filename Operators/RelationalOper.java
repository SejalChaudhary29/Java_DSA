package Operators;

public class RelationalOper {
    public static void main(String[] args) {
        // relational operation give true or false output
        int a = 10 ;
        int b = 20;

        System.out.println(a==b);//false
        System.out.println(a<b);//true
        System.out.println(a>b);//false
        System.out.println(a<=b);//true
        System.out.println(a>=b);//false
        System.out.println(a!=b);//true
    }
}
