package Operators;

public class AssigmentOper {
    public static void main(String[] args) {
        /* Assignment Operator
          
        =, +=,-=,*=,/=

         */
        int a = 20;
        a += 90;
        System.out.println(a);//110

        int b = 30;
        b-= 10;
        System.out.println(b); //20


        // same for *= and /=
    }
}
